import axios from 'axios'
import Head from 'next/head'
import { useEffect } from 'react'
import { useDispatch, useSelector } from 'react-redux'
import About from '../components/About'
import Homee from '../components/Home'
import Projects from '../components/Projects'
import Skills from '../components/Skills'
import Testimonials from '../components/Testimonials'
import useMediaQuery from '../hooks/MediaQuery'
import { setFontSize, setMyProjects, setTechnologies , setUser } from '../state/index'
import LoginModal from '../components/Modal/LoginModal'
import RegisterModal from '../components/Modal/RegisterModal'
import { useSession } from 'next-auth/react'

export default function Home({ myprojects , technologies,testimonials}) {
  const dispatch = useDispatch()
  const background = useSelector(state => state.background)
  const login = useSelector(state => state.LoginModal)
  const register = useSelector(state => state.RegisterModal)
  const isAboveMediumScreens = useMediaQuery("(min-width:800px)")
  const session = useSession()
   
  useEffect(() => {

    dispatch(setMyProjects(myprojects))
    dispatch(setTechnologies(technologies))

    if(localStorage.getItem('user')){
      setUser(localStorage.getItem('user'))
      dispatch(setUser(JSON.parse(localStorage.getItem('user'))))
    }

    if(session.data){
      setUser(localStorage.setItem('user' , JSON.stringify(session.data.user)))
      dispatch(setUser(session.data.user))
    }

  }, [session.data , session.data && session.data.user])
  

    useEffect(()=>{
      dispatch(setFontSize({
        xs: isAboveMediumScreens ? '12px' : '10px',
        sm: isAboveMediumScreens ? '14px' : '12px',
        base: isAboveMediumScreens ? '16px' : '14px',
        lg: isAboveMediumScreens ? '18px' : '16px',
        xl: isAboveMediumScreens ? '20px' : '18px',
        xxl: isAboveMediumScreens ? '24px' : '20px',
        xxxl: isAboveMediumScreens ? '30px' : '24px',
        xxxxl: isAboveMediumScreens ? '36px' : '28px',
        xxxxxl: isAboveMediumScreens ? '48px' : '32px',
        xxxxxxl: isAboveMediumScreens ? '64px' : '36px',
       }))
    } , [isAboveMediumScreens])
  
 
  return (
    <div>
      <Head>
        <title>SinghKuldeep - Home</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/logo.jpg" />
      </Head>

    <main style={{background:background.secondary}}> 
    { login && <LoginModal/>}
    { register && <RegisterModal/>}
      <Homee/>
      <Projects projects={myprojects}/>
      <Skills skillsArray={technologies}/>
      <About/>
      <Testimonials testimonials={testimonials}/>
    
     </main>
    
    </div>
  )
}

